sql = []
sql.append('-- MCE Command Center: Agentic & Workflow Layer')
sql.append('CREATE EXTENSION IF NOT EXISTS "uuid-ossp";')

sql.append('-- 1. Audit Logs (Immutable, Append-Only)')
sql.append('CREATE TABLE IF NOT EXISTS audit_logs (')
sql.append('    id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),')
sql.append('    actor_id text NOT NULL, -- Clerk user ID or Agent Name')
sql.append('    action text NOT NULL,')
sql.append('    entity_type text NOT NULL,')
sql.append('    entity_id text NOT NULL,')
sql.append('    diff jsonb,')
sql.append('    created_at timestamptz DEFAULT now()')
sql.append(');')

sql.append('\n-- 2. Workflow Definitions (Versioned State Machines)')
sql.append('CREATE TABLE IF NOT EXISTS workflow_definitions (')
sql.append('    id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),')
sql.append('    name text NOT NULL UNIQUE,')
sql.append('    version text NOT NULL,')
sql.append('    definition jsonb NOT NULL, -- States, Transitions, Guards')
sql.append('    created_at timestamptz DEFAULT now()')
sql.append(');')

sql.append('\n-- 3. Workflow Instances (Running Processes)')
sql.append('CREATE TABLE IF NOT EXISTS workflow_instances (')
sql.append('    id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),')
sql.append('    definition_id uuid REFERENCES workflow_definitions(id),')
sql.append('    current_state text NOT NULL,')
sql.append('    context jsonb DEFAULT "{}"::jsonb,')
sql.append('    status text DEFAULT \'active\', -- active, completed, failed')
sql.append('    created_at timestamptz DEFAULT now(),')
sql.append('    updated_at timestamptz DEFAULT now()')
sql.append(');')

sql.append('\n-- 4. Workflow Events (Event Sourcing Log)')
sql.append('CREATE TABLE IF NOT EXISTS workflow_events (')
sql.append('    id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),')
sql.append('    instance_id uuid REFERENCES workflow_instances(id),')
sql.append('    event_type text NOT NULL,')
sql.append('    payload jsonb,')
sql.append('    created_at timestamptz DEFAULT now()')
sql.append(');')

sql.append('\n-- 5. Enable RLS')
sql.append('ALTER TABLE audit_logs ENABLE ROW LEVEL SECURITY;')
sql.append('ALTER TABLE workflow_definitions ENABLE ROW LEVEL SECURITY;')
sql.append('ALTER TABLE workflow_instances ENABLE ROW LEVEL SECURITY;')
sql.append('ALTER TABLE workflow_events ENABLE ROW LEVEL SECURITY;')

sql.append('\n-- 6. Policies (Public Read for MVP, strict write via functions in prod)')
sql.append('CREATE POLICY "Public Read Audit" ON audit_logs FOR SELECT USING (true);')
sql.append('CREATE POLICY "Public Insert Audit" ON audit_logs FOR INSERT WITH CHECK (true);')
sql.append('CREATE POLICY "Public Read Workflows" ON workflow_definitions FOR SELECT USING (true);')
sql.append('CREATE POLICY "Public Read Instances" ON workflow_instances FOR SELECT USING (true);')
sql.append('CREATE POLICY "Public Insert Instances" ON workflow_instances FOR INSERT WITH CHECK (true);')

print('\n'.join(sql))